/** 英文日期时间字符串格式 */
export const enum DateFormatEn {
  /** 时间日期 */
  Date = 'YYYY-MM-DD',
  /** 时间日期(时) */
  DateHr = 'YYYY-MM-DD HH',
  /** 时间日期(分) */
  DateMin = 'YYYY-MM-DD HH:mm',
  /** 时间日期(秒) */
  DateSec = 'YYYY-MM-DD HH:mm:ss',
  /** 时间日期-紧凑 */
  DateTight = 'YYYYMMDD',
  /** 时间日期(时)-紧凑 */
  DateHrTight = 'YYYYMMDDHH',
  /** 时间日期(分)-紧凑 */
  DateMinTight = 'YYYYMMDDHHmm',
  /** 时间日期(秒)-紧凑 */
  DateSecTight = 'YYYYMMDDHHmmss',
}
